package com.entity;

public class Xitong {
	private int id;
	private int quanxian;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQuanxian() {
		return quanxian;
	}
	public void setQuanxian(int quanxian) {
		this.quanxian = quanxian;
	}
	@Override
	public String toString() {
		return "Xitong [id=" + id + ", quanxian=" + quanxian + "]";
	}
	
	

}
